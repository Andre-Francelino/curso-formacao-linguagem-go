package main

import "fmt"

func main() {

	// sabendo o tamanho da string - conta quantos caracteres exitem incluindo os espaços
	fmt.Println(len("Aula String GO!"))

	// retornando o caractere na posição da string (lendo e retornando o binário do caractere)
	fmt.Println("Aula String GO!"[6])

	// concatenando strings
	fmt.Println("Aula " + "String " + "GO!")

}
